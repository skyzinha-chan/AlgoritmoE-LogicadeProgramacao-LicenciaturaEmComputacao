public class L2_ex1 {

	public static void main(String[] args) {
		int peso2 = 2;
		int peso3 = 3;
		double n1 = 5.6 * peso2;
		double n2 = 8.9 * peso3;
		double n3 = 9.9 * peso3;
		double n4 = 6.7 * peso2;
		double mp = (n1 + n2 + n3 + n4) / 10;
		System.out.println("A média ponderada do aluno é " + mp);

	}

}
